package ex_01_Java_Basics;

public class Lab008_Common_Math {

    public static void main(String[] args) {
        System.out.println(2 + 2);
        System.out.println(2 - 2);
        System.out.println(2 * 2);
        System.out.println(2 / 2);
        System.out.println("2*2"); // double quote  means String
        System.out.println(3/2);  // answer = 1 >> Rule : println -> Integer part --> No Decimal
    }
}
