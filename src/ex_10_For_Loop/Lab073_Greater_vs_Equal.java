package ex_10_For_Loop;

public class Lab073_Greater_vs_Equal {
    public static void main(String[] args) {
        for (int i = 0; i <= 10; i++) { //0 to 10
            System.out.println(i);
            
        }
        for (int i = 1; i <= 10; i++) { // 1 to 10
            System.out.println(i);

        }
        for (int i = 0; i < 10; i++) { //1 to 9
            System.out.println(i);

        }
    }
}
