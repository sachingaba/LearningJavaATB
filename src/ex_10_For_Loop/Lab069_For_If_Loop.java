package ex_10_For_Loop;

public class Lab069_For_If_Loop {
    public static void main(String[] args) {
        for (int age = 0; age <18 ; age++) {
            if (age>15){
                System.out.println(age + " gift");
            }else {
                System.out.println("no gift");
            }

        }
    }
}
