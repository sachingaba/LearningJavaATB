package ex_10_For_Loop;

public class Lab065_For_Loop {
    public static void main(String[] args) {
        //for loop
        // initialization -> Condition -> update
        int i;
        for(i=0;i<10;i++){
            System.out.println(i);
        }
        for(i=0;i<10;++i){
            System.out.println(i);
        }
    }
}
