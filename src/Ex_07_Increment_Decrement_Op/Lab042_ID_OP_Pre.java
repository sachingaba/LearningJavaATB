package Ex_07_Increment_Decrement_Op;

public class Lab042_ID_OP_Pre {

        public static void main(String[] args) {


            int a = 10;
            System.out.println(a); // Original  value of a
            int b = ++a; // ++a--> a= a+1
            System.out.println(a); // now a value change to a=a+1
            System.out.println(b); // b = a+1

    }
}
