package ex_14_Strings;

public class Lab098_Strings_Functions {
    public static void main(String[] args) {
        char c = 'A';
        System.out.println(c);
        String s1 = "ABCD"; //String 1
        System.out.println(s1);
        System.out.println(s1.length());
        System.out.println(s1.toLowerCase()); //String 2
        System.out.println(s1.toUpperCase()); //String 3
        System.out.println(s1.concat("E")); // String 4

    }
}
